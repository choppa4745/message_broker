-- ============================================================================
-- «Грязное чтение» (DIRTY READ) и PostgreSQL
-- ============================================================================
--
-- В PostgreSQL даже при SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
-- фактически используется не ниже READ COMMITTED (документация PG).
-- Незакоммиченные изменения другой транзакции нельзя «увидеть» в SELECT.
-- При попытке читать строку, заблокированную незавершённым UPDATE,
-- читающая транзакция будет ЖДАТЬ (или получит снимок старых данных для
-- своего запроса — в зависимости от вида запроса и блокировок).
--
-- Демонстрация отсутствия dirty read:
--
-- --- Сессия A ---
-- BEGIN;
-- UPDATE accounts SET balance = 7777.77 WHERE id = 1;
-- -- НЕ COMMIT / ROLLBACK
--
-- --- Сессия B ---
-- BEGIN;
-- SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;  -- в PG всё равно RC
-- SELECT balance FROM accounts WHERE id = 1;
--   -- Ожидание блокировки до COMMIT/ROLLBACK A ИЛИ отображение прежнего
--   -- закоммиченного значения (в зависимости от сценария MVCC; обычно B ждёт
--   -- строковую блокировку на UPDATE в A для своей версии чтения).
--
-- После ROLLBACK в A «грязное» 7777.77 исчезнет; B никогда не должна была
-- закономерно показать 7777.77 как закоммиченное значение.
--
-- Вывод для отчёта: классическое dirty read на PostgreSQL воспроизвести нельзя;
-- для учебного сравнения используйте СУБД, где есть настоящий READ UNCOMMITTED
-- (например, InnoDB всё равно часто эквивалентен RC — уточняйте документацию).
--
-- Как избежать dirty read в СУБД, где оно возможно: повысить уровень изоляции
-- минимум до READ COMMITTED (как в PG по умолчанию).
-- ============================================================================

