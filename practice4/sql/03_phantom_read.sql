-- ============================================================================
-- Аномалия: PHANTOM READ (фантомное чтение)
-- Демонстрируется на READ COMMITTED: второй подсчёт в той же транзакции
-- «видит» новые строки, появившиеся после коммита другой сессии.
-- ============================================================================
--
-- Подготовка: 01_schema_and_seed.sql (у Alice id=1 есть хотя бы один order).
--
-- --- Сессия A ---
-- BEGIN;
-- SET TRANSACTION ISOLATION LEVEL READ COMMITTED;
-- SELECT COUNT(*) FROM orders WHERE account_id = 1;
--   -- запомните число (после seed обычно 1)
-- -- Перейдите в Сессию B.
--
-- --- Сессия B ---
-- BEGIN;
-- INSERT INTO orders (account_id, amount) VALUES (1, 25.00);
-- COMMIT;
--
-- --- Снова Сессия A ---
-- SELECT COUNT(*) FROM orders WHERE account_id = 1;
--   -- счётчик увеличился — «фантомная» новая строка для первого снимка логики A
-- COMMIT;
--
-- Ожидаемый результат: два COUNT(*) в одной транзакции A дали разные значения.
--
-- Замечание: в PostgreSQL при уровне REPEATABLE READ второй SELECT в той же
-- транзакции увидит тот же снимок (число не изменится) — фантомы подавлены
-- снимком. Для сравнения повторите демо с:
--   SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;
-- во втором прогоне — COUNT не должен измениться внутри транзакции A.
--
-- Как избежать фантомов при необходимости стабильного диапазона: SERIALIZABLE,
-- REPEATABLE READ (snapshot), или предикатные блокировки / materialized view
-- в зависимости от задачи.
-- ============================================================================

SELECT COUNT(*) AS orders_for_alice FROM orders WHERE account_id = 1;
