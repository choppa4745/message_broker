-- ============================================================================
-- Аномалия: LOST UPDATE (потерянное обновление)
-- Два сеанса читают одно и то же значение и записывают «на основе прочитанного»,
-- одно из обновлений логически теряется (последний COMMIT побеждает неправильно).
-- ============================================================================
--
-- Восстановите данные перед демо:
--   UPDATE accounts SET balance = 1000.00, version = 0 WHERE id = 1;
--
-- Важно: «потерянное обновление» здесь моделируется как в клиентском коде,
-- который подставляет УСТАРЕВШИЙ прочитанный баланс, а не делает
-- UPDATE balance = balance + 50 (у последнего в PG возьмётся актуальная
-- строка — вклад B тогда сложится, и аномалия не проявится).
--
-- --- Сессия A ---
-- BEGIN;
-- SELECT balance FROM accounts WHERE id = 1;
--   -- запомните 1000.00 как «прочитанный в приложении» (не коммитьте A)
-- -- Перейдите в B, выполните её шаги, вернитесь в A
--
-- --- Сессия B ---
-- BEGIN;
-- SELECT balance FROM accounts WHERE id = 1;
-- UPDATE accounts SET balance = balance + 100 WHERE id = 1;  -- фиксируем 1100.00
-- COMMIT;
--
-- --- Снова Сессия A ---
-- -- Клиент думает: было 1000, добавить 50 → записать 1050 (игнорируя +100 из B)
-- UPDATE accounts SET balance = 1050.00 WHERE id = 1;
-- COMMIT;
--
-- --- Проверка ---
-- SELECT balance FROM accounts WHERE id = 1;
--
-- Ожидаемый эффект: логически ждали 1150 (1000+100+50 согласованно), фактически
-- 1050 — вклад сессии B (+100) «потерян» после записи A с устаревшим основанием.
--
-- Как избежать:
--   - SELECT ... FOR UPDATE при чтении перед UPDATE (блокировка строки)
--   - оптимистичные блокировки: UPDATE ... WHERE id = ? AND version = ?
--   - атомарное выражение: UPDATE accounts SET balance = balance + 50
--   - SERIALIZABLE (может выдать serialization failure, нужен retry)
-- ============================================================================

UPDATE accounts SET balance = 1000.00, version = 0 WHERE id = 1;
